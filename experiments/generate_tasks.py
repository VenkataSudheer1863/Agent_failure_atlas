TASK_LISTS = {
    "information_seeking": INFORMATION_SEEKING_TASKS,
    "tool_use": TOOL_USE_TASKS,
    "planning": PLANNING_TASKS,
    "reasoning": REASONING_TASKS,
    "multi_agent": MULTI_AGENT_TASKS,
}


def generate_task_files(output_dir: str = "experiments/tasks") -> None:
    out = Path(output_dir)
    out.mkdir(parents=True, exist_ok=True)

    for task_type, prompts in TASK_LISTS.items():
        filepath = out / f"{task_type}.jsonl"
        records = []
        for i, prompt in enumerate(prompts[:50], start=1):
            records.append({
                "id": f"{task_type.upper()[:4]}-{i:03d}",
                "task_type": task_type,
                "prompt": prompt,
                "difficulty": random.choice(["easy", "medium", "hard"]),
            })

        with open(filepath, "w", encoding="utf-8") as f:
            for r in records:
                f.write(json.dumps(r) + "\n")
        print(f"Saved {len(records)} {task_type} tasks to {filepath}")

    print(f"\nAll task files saved to {output_dir}")


if __name__ == "__main__":
    generate_task_files()
